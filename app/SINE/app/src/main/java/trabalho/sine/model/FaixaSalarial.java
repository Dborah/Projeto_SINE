package trabalho.sine.model;

public class FaixaSalarial {
    private Double trainne;
    private Double junior;
    private Double pleno;
    private Double senior;
    private Double master;

    public Double getTrainne() {
        return trainne;
    }

    public void setTrainne(Double trainne) {
        this.trainne = trainne;
    }

    public Double getJunior() {
        return junior;
    }

    public void setJunior(Double junior) {
        this.junior = junior;
    }

    public Double getPleno() {
        return pleno;
    }

    public void setPleno(Double pleno) {
        this.pleno = pleno;
    }

    public Double getSenior() {
        return senior;
    }

    public void setSenior(Double senior) {
        this.senior = senior;
    }

    public Double getMaster() {
        return master;
    }

    public void setMaster(Double master) {
        this.master = master;
    }
}
